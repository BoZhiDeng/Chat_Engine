package com.BZD.Chat_Engine_Demo.Chat;

/**
 * Enum representing the types of messages that can be sent in the chat.
 * CHAT - a standard chat message.
 * JOIN - a notification that a user has joined the chat.
 * LEAVE - a notification that a user has left the chat.
 */
public enum MessageType {
    CHAT,
    JOIN,
    LEAVE
}
